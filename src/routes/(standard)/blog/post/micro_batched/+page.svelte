

<article>
    <h1>What does it mean to be "Micro Batched"</h1>
    <p>
        We like to call our candy "Micro Batched" to stress how hand crafted and small each bach really is. 
        Most of our batches of candy only make three pounds at a time and everything is made to order.
    </p>
    <h3 class="image_thing">
        <span>
            So did you just make up the phrase Micro Batched?
        </span>
        <img src="https://imagedelivery.net/9cMe9d4LFXgI4nPSeZlAjw/23955bc9-bf42-4cb8-3fca-5498c17e3e00/public" alt="toffee">
    </h3>
    <p>
        In a way yes, there's no criteria of what makes something small batch vs micro vs mini or even a giant batch. 
        We use that word to differentiate our process from other "Small Batch" 
        candy makers. These so called "Small batch" companies really only use 
        large industrial processes that yield 70+ pound batches or worse yet use
        assembly lines to make the candy and never even hand make anything.
    </p>
    <p>
        Our Toffee journey started out by simply making some toffee for friends. 
        We will to keep that same handmade artisanal touch for as long as Colorado Toffee is making toffee
    </p>
