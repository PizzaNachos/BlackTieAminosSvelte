    <h1>What is Toffee?</h1>
    <h3>
        How is it different than brittle, taffy, butterscotch, and caramel?
    </h3>
    <p>
        Toffee is a classic crunchy candy made from sugar and butter. 
        It is normally cooked with nuts of some sort and covered in chocolate.
        No one is quite sure where toffee truly comes from but in america we generally
        call it "English Style" toffee (even though its almost certainly not from England).
        Due to the high heat that toffee is cooked to, it develops a deep caramelized flavor
        and richness.
    </p>
    <p>
        Brittle is an even more vague term that usually means a hard candy with nuts in it. 
        The primary difference between classic brittle versus classic toffee is the inclusion of butter.
        Toffee will have a substantial amount of butter cooked into the candy while brittle generally contains
        no butter at all. This gives brittle a harder and more brittle (hence the name) glass like texture.
        We think toffee is always better than brittle and thats why even on our <a href="/products/pecan">Pecan Brittle</a>
        we cook it as if it was pecan toffee (only difference is we don't top it with chocolate)
    </p>
    <p>
        Taffy is a chewy candy that is cooked to much lower temperatures and then stretched and folded many times over 
        to give it a airy and chewy texture. Commonly taffy will have flavors added and is also called salt water taffy.
    </p>
    <p>Butterscotch is similar to toffee with several key differences.</p>
        <ul>
            <li>No nuts 
                <p>Butterscotch will normally be flavored with other ad-ins or nothing at all.</p>
            </li>
            <li>Cooked soft 
                <p>While some butterscotches you can get at 
                    supermarkets are hard and crunchy 
                    (and artificially flavored) we think that soft cooked
                    chewy butterscotch is the best you can get.
                 </p>
            </li>
            <li>Molasses
                <p>
                    Butterscotch contains a decent amount of molasses (normally in the form of brown sugar)
                    that gives it a deeper richer flavor without needed to caramelize the sugar nearly as much
                </p>
            </li>
        </ul> 
</article>

