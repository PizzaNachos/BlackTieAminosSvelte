<article>
    <h1>What does it mean to be "Handmade"</h1>
    <p>
        The FDA unfortunately does not provide any legal definition nor enforcement of what a handmade good is.
        We like to think it means that every step, every process, and every stage of making a 
        product is personally and physically done by an expert artisan.
    </p>
    <p>
        Some giant companies claim to be "handmade" when all they 
        really mean is the machines that make their candy is operated by a person.
    </p>
    <h3 class="image_thing">
        <img src="https://imagedelivery.net/9cMe9d4LFXgI4nPSeZlAjw/c936fa84-2b63-4f57-3167-b8112a88a300/public" alt="Ingredients in Spicy Chocolate Almond Toffee">
        <span>
           We hand make everything here in Colorado
        </span>
    </h3>
    <p>
       We hand make the toffee, stirring every batch until it is as hot and bubbly as possible without burning 
       (so hot and fast no thermometer can give an accurate reading, We've measured well over 300°f). 
       We lather each batch of toffee in chocolate and toss toasted almonds on top of it.
       We then hand hand pack, and hand ship your delicious candy all from Colorful Colorado (a very manual process).
    </p>
    <h2>Why hand made is better</h2>
    <p>
        Because we hand make every batch, our quality control is amazing. The number of products we can make is nearly endless compared to other toffee makers.
        You'll see that we have many different candies and many different chocolates for purchase. That's only possible because we still hand make every order.
    </p>
